export async function seedFoods(database: any): Promise<void> {
  const existing = await database.query(sql`SELECT COUNT(*) as count FROM foods`);
  if (existing[0]?.count > 0) return;

  for (const food of GHANAIAN_FOODS) {
    await database.query(sql`
      INSERT INTO foods (id, name, category, calories_per_serving, protein_g, carbs_g, fats_g, serving_size, serving_weight_g, tags, image_url, glycemic_index, prep_method, region, cost_tier, pairs_with)
      VALUES (${food.id}, ${food.name}, ${food.category}, ${food.calories_per_serving}, ${food.protein_g}, ${food.carbs_g}, ${food.fats_g}, ${food.serving_size}, ${food.serving_weight_g}, ${JSON.stringify(food.tags)}, ${food.image_url}, ${food.glycemic_index ?? null}, ${food.prep_method ?? null}, ${JSON.stringify(food.region ?? [])}, ${food.cost_tier ?? null}, ${JSON.stringify(food.pairs_with ?? [])})
    `);
  }

  console.log(`[DB] Seeded ${GHANAIAN_FOODS.length} Ghanaian foods`);
}
